# Function to visualize feature distribution with respect to target variable
def distribution_plot_wrt_target(data, predictor, target):

    """
    Plot the distribution of a feature split by the target variable

    Parameters
    data: dataframe
    feature: feature
    target: target variable
    """

    # Creating subplots for histogram and boxplots
    fig, axs = plt.subplots(2, 2, figsize = (12, 10))

    # Getting unique target classes
    target_unique = data[target].unique()

    # Histogram for first target class
    axs[0, 0].set_title(f'Distribution of the {predictor}  for target=' + str(target_unique[0]))
    sns.histplot(
        data = data[data[target] == target_unique[0]],
        x = predictor,
        kde = True,
        ax = axs[0, 0],
        color = 'teal',
        stat = 'density',
    )

    # Histogram for second target class
    axs[0, 1].set_title(f'Distribution of the {predictor} for target=' + str(target_unique[1]))
    sns.histplot(
        data = data[data[target] == target_unique[1]],
        x = predictor,
        kde = True,
        ax = axs[0, 1],
        color = 'orange',
        stat = 'density'
    )

    # Boxplot with outliers
    axs[1, 0].set_title('Boxplot with respect to target')
    sns.boxplot(data = data, x = target, y = predictor, ax = axs[1, 0], palette = 'gist_rainbow')

    # Boxplot without outliers
    axs[1, 1].set_title('Boxplot (Without Outliers) with respect to target')
    sns.boxplot(data = data, x = target, y = predictor, ax = axs[1, 1], showfliers = False, palette = 'gist_rainbow')

    # Adjust layout and display plots
    plt.tight_layout()
    plt.show()

# Function to show target distribution across categorical feature levels
def stacked_barplot(data, predictor, target):

    """
    Print the category counts and plot a stacked bar chart

    Parameters
    data: dataframe
    predictor: independent variable
    target: target variable
    """

    # Getting number of unique categories
    count = data[predictor].nunique()

    # Selecting target class for sorting categories
    sorter = data[target].value_counts().index[-1]

    # Creating normalized cross tabulation
    tab1 = pd.crosstab(data[predictor], data[target], normalize = 'index').sort_values(
        by = sorter, ascending = False
    )

    # Displaying category wise target proportions
    print(tab1)
    print('-' * 120)

    # Creating stacked barplot
    tab = pd.crosstab(data[predictor], data[target], normalize = 'index').sort_values(
        by = sorter, ascending = False
    )
    tab.plot(kind = 'bar', stacked = True, figsize = (count + 5, 5))

    # Positioning legend outside the plot
    plt.legend(loc = 'lower left', frameon = False)
    plt.legend(loc = 'upper left', bbox_to_anchor = (1, 1))

    # Displaying the plot
    plt.show()

