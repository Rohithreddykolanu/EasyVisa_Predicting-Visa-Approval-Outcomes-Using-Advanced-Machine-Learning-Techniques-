# Function to plot histogram and boxplot for numerical features
def histogram_boxplot(data, feature, figsize = (12, 7), kde = False, bins = None):

    """
    Boxplot and histogram combined

    Parameters
    data: dataframe
    feature: dataframe column
    figsize: size of figure (default (12,7))
    kde: whether to show the density curve (default False)
    bins: number of bins for histogram (default None)
    """

    # Creating boxplot and histogram in one figure
    f2, (ax_box2, ax_hist2) = plt.subplots(
        nrows = 2,
        sharex = True,
        gridspec_kw = {'height_ratios': (0.25, 0.75)},
        figsize = figsize,
    )

    # Boxplot to identify spread and outliers
    sns.boxplot(
        data = data, x = feature, ax = ax_box2, showmeans = True, color = 'violet'
    )

    # Histogram to understand feature distribution
    sns.histplot(
        data = data, x = feature, kde = kde, ax = ax_hist2, bins = bins, palette = 'winter'
    ) if bins else sns.histplot(
        data = data, x = feature, kde = kde, ax = ax_hist2
    )

    # Adding mean and median reference lines
    ax_hist2.axvline(
        data[feature].mean(), color = 'green', linestyle = '--'
    )

    ax_hist2.axvline(
        data[feature].median(), color = 'black', linestyle = '-'
    )

# Function to create a labeled barplot for categorical features
def labeled_barplot(data, feature, perc = False, n = None):

    """
    Barplot with percentage at the top

    Parameters
    data: dataframe
    feature: dataframe column
    perc: whether to display percentages instead of count

    n: displays the top n category levels (default = None, i.e., display all levels)
    """

    # Calculating total records and number of categories
    total = len(data[feature])
    count = data[feature].nunique()

    # Setting figure size based on number of categories
    if n is None:
      plt.figure(figsize = (count + 1, 5))
    else:
      plt.figure(figsize = (n + 1, 5))

    # Creating countplot for category distribution
    plt.xticks(rotation = 90, fontsize = 15)
    ax = sns.countplot(
        data = data, x = feature, palette = 'Paired', order = data[feature].value_counts().index[:n].sort_values()
    )

    # Adding count or percentage labels on bars
    for p in ax.patches:
      if perc == True:
        label = "{:.1f}%".format(
            100 * p.get_height() / total
        )
      else:
        label = p.get_height()

      x = p.get_x() + p.get_width() / 2
      y = p.get_height()

      ax.annotate(
          label,
          (x, y),
          ha = 'center',
          va = 'center',
          size = 12,
          xytext = (0, 5),
          textcoords = 'offset points'
      )

    # Displaying the plot
    plt.show()

